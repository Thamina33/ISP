package com.example.isp;

public class modelForProfile {


    String name , ip , phone , uid  ;

    public modelForProfile() {
    }

    public modelForProfile(String name, String ip, String phone, String uid) {
        this.name = name;
        this.ip = ip;
        this.phone = phone;
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}
